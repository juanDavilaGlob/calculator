public class Main {
    public static void main(String[] args) {

    }

    /**
     * Suma dos numeros
     * @param n1
     * @param n2
     * @return
     */
    private int sum(int n1, int n2){
        return n1 + n2;
    }

    /**
     * Multiplica 2 numeros
     * @param n1
     * @param n2
     * @return
     */
    private int mult(int n1, int n2){
        return n1 * n2;
    }
    
    /**
     * Resta dos numeros
     * @param n1
     * @param n2
     * @return
     */
    private int sub(int n1, int n2) {
        return n1 - n2;
    }

    /**
     * Divide 2 numeros
     * @param n1
     * @param n2
     * @return
     */
    private int div(int n1, int n2){
        return n1 / n2;
    }
}
